public class NomineeInfo {
    String name;
    String email;
    String isSenior;
    String hsGradFrom;

    public NomineeInfo(String name, String email, String isSenior, String hsGradFrom) {
        this.name = name;
        this.email = email;
        this.isSenior = isSenior;
        this.hsGradFrom = hsGradFrom;
    }
}
