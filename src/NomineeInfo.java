public class NomineeInfo {
    String name;
    String email;
    String isSenior;
    String hsGradFrom;
    Double distanceToDrive;

    public NomineeInfo(String name, String email, String isSenior, String hsGradFrom, Double distanceToDrive) {
        this.name = name;
        this.email = email;
        this.isSenior = isSenior;
        this.hsGradFrom = hsGradFrom;
        this.distanceToDrive = distanceToDrive;
    }
}
