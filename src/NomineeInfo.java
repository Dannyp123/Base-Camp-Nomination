public class NomineeInfo {
    String name;
    String email;
    String isSenior;
    String hsGradFrom;
    Double distanceToDrive;
    String attendance;

    public NomineeInfo(String name, String email, String isSenior, String hsGradFrom, Double distanceToDrive, String attendance) {
        this.name = name;
        this.email = email;
        this.isSenior = isSenior;
        this.hsGradFrom = hsGradFrom;
        this.distanceToDrive = distanceToDrive;
        this.attendance = attendance;
    }
}
