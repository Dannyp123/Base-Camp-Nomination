
    public class NomineeInformation {
        String name;
        String email;
        String hsGradFrom;
        Double distanceToDrive;
        String number;
        String contactMethod;
        String question;


        public NomineeInformation(String name, String email, String hsGradFrom, Double distanceToDrive, String number, String contactMethod, String question) {
            this.name = name;
            this.email = email;
            this.hsGradFrom = hsGradFrom;
            this.distanceToDrive = distanceToDrive;
            this.number = number;
            this.contactMethod = contactMethod;
            this.question = question;

        }
    }

